////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step=1) {
  let output = [];
  if (start === end) {
    return output;
  } if (step > 0) {
      for (let i = start; i <= end; i += step)
      output.push(i);
    } else {
      for (let i = start; i >= end; i += step)
      output.push(i);
  }
  return output;
}

////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(nums) {
  total = 0;
  for (let i = 0; i <= nums.length; i++) {
    total += i;
  }
  return total;
}

